package modulo6ejercicio1;

public class Persona {

	private int dni;
	private String nombre;
	
	public Persona() {}
	
	public Persona(int dni) {
		this.dni = dni;
	}

	public int getDni() {
		return dni;
	}

	public void setDni(int dni) {
		this.dni = dni;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
	
	
	
}

package com.globallogic.app;

public class TiposPrimitivos {

	public static void main( String[] args ) {
		
		int myNum = 15;
		char myChar = 'a';
		boolean b = true;
		float myFloat = 2436.2456f;
		short myShort = 500;
		
		System.out.println("El valor de mi numero es " + myNum);
		System.out.println("El valor de caracter es " + myChar);
		System.out.println("El valor de b es " + b);
		System.out.println("El valor de mifloat es " + myFloat);
		System.out.println("El valor de mishort es " + myShort);
    }
}
